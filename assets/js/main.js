const chatbox = document.querySelector(".chatbox");
const chatInput = document.querySelector(".chat-input textarea");
const sendChatBtn = document.querySelector(".chat-input span");
const submitRecommend = document.getElementById("submitBut");

let userMessage = null; // Variable to store user's message
const inputInitHeight = chatInput.scrollHeight;

const createChatLi = (message, className) => {
  // Create a chat <li> element with passed message and className
  const chatLi = document.createElement("li");
  chatLi.classList.add("chat", `${className}`);
  let chatContent = className === "outgoing" ? `<p></p>` : `<span class="material-symbols-outlined">smart_toy</span><p></p>`;
  chatLi.innerHTML = chatContent;
  chatLi.querySelector("p").textContent = message;
  return chatLi; // return chat <li> element
}

const generateResponse = (chatElement, userMessage) => {
  const messageElement = chatElement.querySelector("p");
  const API_URL = "http://127.0.0.1:5000/chatbot?query=" + userMessage;

  // Define the properties and message for the API request
  const requestOptions = {
    method: "GET",
    headers: {
      "Content-Type": "application/json",
    },
  };

  // Send GET request to API, get response and set the reponse as paragraph text
  fetch(API_URL, requestOptions).then(res => res.json()).then(data => {
    messageElement.textContent = data['response'];
  }).catch(() => {
    messageElement.classList.add("error");
    messageElement.textContent = "Oops! Something went wrong. Please try again.";
  }).finally(() => chatbox.scrollTo(0, chatbox.scrollHeight));
}

const handleChat = () => {
  userMessage = chatInput.value.trim(); // Get user entered message and remove extra whitespace
  if (!userMessage) return;

  // Clear the input textarea and set its height to default
  chatInput.value = "";
  chatInput.style.height = `${inputInitHeight}px`;

  // Append the user's message to the chatbox
  chatbox.appendChild(createChatLi(userMessage, "outgoing"));
  chatbox.scrollTo(0, chatbox.scrollHeight);

  setTimeout(() => {
    // Display "Thinking..." message while waiting for the response
    const incomingChatLi = createChatLi("Thinking...", "incoming");
    chatbox.appendChild(incomingChatLi);
    chatbox.scrollTo(0, chatbox.scrollHeight);
    generateResponse(incomingChatLi, userMessage);
  }, 600);
}

chatInput.addEventListener("input", () => {
  // Adjust the height of the input textarea based on its content
  chatInput.style.height = `${inputInitHeight}px`;
  chatInput.style.height = `${chatInput.scrollHeight}px`;
});

chatInput.addEventListener("keydown", (e) => {
  // If Enter key is pressed without Shift key and the window 
  // width is greater than 800px, handle the chat
  if (e.key === "Enter" && !e.shiftKey && window.innerWidth > 800) {
    e.preventDefault();
    handleChat();
  }
});


var x = document.getElementById("location");

function getLocation() {
  if (navigator.geolocation) {
    navigator.geolocation.getCurrentPosition(showPosition);
  } else {
    x.innerHTML = "Geolocation is not supported by this browser.";
  }
}

var location_indx;
function showPosition(position) {
  location_indx = position.coords.latitude + " " + position.coords.longitude;
  x.innerHTML = "Latitude: " + position.coords.latitude +
    "<br>Longitude: " + position.coords.longitude;
}

const submit = () => {
  getLocation();
  size = document.getElementById("size");

  if (location_indx == null || size.value == null) return;
  const API_URL = "http://127.0.0.1:5000/recommend?size=" + size.value + "&" + "location=" + location_indx;

  // Define the properties and message for the API request
  const requestOptions = {
    method: "GET",
    headers: {
      "Content-Type": "application/json",
    },
  };

  // Send GET request to API, get response and set the reponse as paragraph text
  fetch(API_URL, requestOptions).then(res => res.json()).then(data => {
    var ap = document.getElementById("addProduct");
    ap.innerHTML = "";
    console.log(data['product']);
    for (var i in data['product']) {
      // URL of the page containing the <img> tag
      console.log(data['product'][i]);
      ap.innerHTML += `
        <a href="`+ data['product'][i]["URL"] +`" target='_blank' style='text-decoration: none;'><div class="row" class="recommend">
          <div class="col-lg-10" style="margin: auto;" data-aos="zoom-in" data-aos-delay="100">
            <div class="member d-flex align-items-start">
              <div class="pic"><img src="`+data['product'][i]['image_URL'] +`" class="img-fluid" alt=""></div>
              <div class="member-info">
                <h2>`+data['product'][i]['Brand'] +`</h2>
                <span>Model: `+data['product'][i]['Model Name'] +` </span>
                <p style="font-size: 16px;">Capacity in Tons : `+data['product'][i]['Capacity in Tons'] +`</p>
              </div>
            </div>
          </div>
        </div><a><br><br>
      `;
    }
  }).catch(() => {
    alert("error");
  });
}

